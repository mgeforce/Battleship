package pp.battleship.bs;

/**
 * Die Klasse repraesetiert eine Exception, welche geworfen wird, falls sich
 * eine Kollision beim Setzen der Schiffe ergibt.<br>
 * 
 * @author Thomas Aichinger, Matthias Bernloehr
 * 
 */
public class IllegalFieldException extends Exception {

	/**
	 * Serial Version User ID
	 */
	private static final long serialVersionUID = 1L;

	public IllegalFieldException() {
		super();
	}

	public IllegalFieldException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IllegalFieldException(String arg0) {
		super(arg0);
	}

	public IllegalFieldException(Throwable arg0) {
		super(arg0);
	}

}
